    {
        $gift = new Gift();
        $gift->plan_id = $plan->id;

        try {
            $gift->save();
        } catch (\Throwable $th) {
            return back()->withErrors('登録処理に失敗しました');
        }

        return redirect()->route('plans.show', compact('plan'))->with('notice', 'リターンを登録しました');
    }

